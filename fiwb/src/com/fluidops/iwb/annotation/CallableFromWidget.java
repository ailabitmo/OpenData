package com.fluidops.iwb.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * CodeExecutionWidget allows methods to be called from
 * wiki widgets. Due to security restrictions, only methods
 * that are marked with this annotation are allowed to be called
 * (and not System.exit for instance)
 * 
 * @author aeb
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface CallableFromWidget
{

}
